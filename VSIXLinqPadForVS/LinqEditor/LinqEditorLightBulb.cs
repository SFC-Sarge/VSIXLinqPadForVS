        /// </item>
        /// </list>
        /// </remarks>
        public Task<bool> HasSuggestedActionsAsync(ISuggestedActionCategorySet requestedActionCategories, SnapshotSpan range, CancellationToken cancellationToken)
        {
            return Task.Factory.StartNew(() =>
            {
                TextExtent extent;
                if (TryGetWordUnderCaret(out extent))
                {
                    // don't display the action if the extent has whitespace
                    return extent.IsSignificant;
                }
                return false;
            });
        }
        /// <summary>Synchronously returns a list of suggested actions for a given span of text.</summary>
        /// <param name="requestedActionCategories">A set of suggested action categories requested.</param>
        /// <param name="range">A span of text in the <see cref="T:Microsoft.VisualStudio.Text.ITextBuffer" /> over which to return suggested actions.</param>
        /// <param name="cancellationToken">A cancellation token that allows to cancel getting list of suggested actions.</param>
        /// <returns>A list of suggested actions or null if no actions can be suggested for a given span of text in the <see cref="T:Microsoft.VisualStudio.Text.ITextBuffer" /></returns>
        /// <remarks>
        /// <para><b>History:</b></para>
        /// <list type="table">
        /// <item>
        /// <description><b>Code Changed by:</b><para>Danny C. McNaught</para><para><para><a href="mailto:danny.mcnaught@dannymcnaught.com">danny.mcnaught@dannymcnaught.com</a></para></para></description>
        /// </item>
        /// <item>
        /// <description><b>Code changed on Visual Studio Host Machine:</b><para>WINDOWS11DEV</para></description>
        /// </item>
        /// <item>
        /// <description><b>Code Change Date and Time:</b><para>Wednesday, January 26, 2022 17:16</para><b>Code Changes:</b><para>Created XML Comment</para></description>
        /// </item>
        /// </list>
        /// </remarks>
        public IEnumerable<SuggestedActionSet> GetSuggestedActions(ISuggestedActionCategorySet requestedActionCategories, SnapshotSpan range, CancellationToken cancellationToken)
        {
            TextExtent extent;
            if (TryGetWordUnderCaret(out extent) && extent.IsSignificant)
            {
                ITrackingSpan trackingSpan = range.Snapshot.CreateTrackingSpan(extent.Span, SpanTrackingMode.EdgeInclusive);
                var upperAction = new LinqUpperCaseSuggestedAction(trackingSpan);
                var lowerAction = new LinqLowerCaseSuggestedAction(trackingSpan);
                return new SuggestedActionSet[] { new SuggestedActionSet(new ISuggestedAction[] { upperAction, lowerAction }) };
            }
            return Enumerable.Empty<SuggestedActionSet>();
        }
